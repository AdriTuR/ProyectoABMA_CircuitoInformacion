package com.example.aturrub.circuitodeinformacion_android;

public class Medicion {
//----------------------------------------------------------------------------------------------//
//----------------------------------------ATRIBUTOS----------------------------------------------//
    private String tiempo;
    private String sensor;
    private String valor;

//----------------------------------------------------------------------------------------------//
//----------------------------------------METODOS-----------------------------------------------//

//---------------------------------------------------------------------//
//--------------------------Medicion()---------------------------------//
//
//     <String>, <String>, <String> --> Medicion() -->
//
//---------------------------------------------------------------------//
//---------------------------------------------------------------------//
    public Medicion(String tiempo, String sensor, String valor) {
        this.tiempo = tiempo;
        this.sensor = sensor;
        this.valor = valor;
    }
//---------------------------------------------------------------------//
//--------------------------getTiempo()--------------------------------//
//
//                      --> Medicion() --> <String>
//
//---------------------------------------------------------------------//
//---------------------------------------------------------------------//
    public String getTiempo() {
        return tiempo;
    }
    //---------------------------------------------------------------------//
//--------------------------getSensor()--------------------------------//
//
//                      --> Medicion() --> <String>
//
//---------------------------------------------------------------------//
//---------------------------------------------------------------------//
    public String getSensor() {
        return sensor;
    }
//---------------------------------------------------------------------//
//--------------------------getValor()---------------------------------//
//
//                      --> Medicion() --> <String>
//
//---------------------------------------------------------------------//
//---------------------------------------------------------------------//
    public String getValor() {
        return valor;
    }
//---------------------------------------------------------------------//
//---------------------------------------------------------------------//
}
